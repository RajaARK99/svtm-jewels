// Helper to parse date strings in DD-Mmm-YY format
function parseDate(dateStr: string): string {
	if (!dateStr || dateStr.trim() === "")
		return new Date().toISOString().split("T")[0];

	try {
		// Format: DD-Mmm-YY
		const parts = dateStr.split("-");
		if (parts.length !== 3) return new Date().toISOString().split("T")[0];

		const day = parts[0];
		const monthStr = parts[1];
		const yearStr = parts[2];

		const months: { [key: string]: number } = {
			Jan: 1,
			Feb: 2,
			Mar: 3,
			Apr: 4,
			May: 5,
			Jun: 6,
			Jul: 7,
			Aug: 8,
			Sep: 9,
			Oct: 10,
			Nov: 11,
			Dec: 12,
		};

		const month = months[monthStr];
		if (!month) return new Date().toISOString().split("T")[0];

		const year = Number.parseInt(yearStr);
		const fullYear = year > 50 ? 1900 + year : 2000 + year;

		const date = new Date(fullYear, month - 1, Number.parseInt(day));
		return date.toISOString().split("T")[0];
	} catch {
		return new Date().toISOString().split("T")[0];
	}
}

async function seedEmployees() {
	try {
		console.log(" Starting to seed employee data...\n");

		// Fetch all reference data
		console.log(" Loading reference data...");

		const jobTitles = await db.select().from(jobTitle);
		const businessUnits = await db.select().from(businessUnit);
		const departments = await db.select().from(department);
		const locations = await db.select().from(location);
		const legalEntities = await db.select().from(legalEntity);
		const users = await db.select().from(user);

		console.log(`   Job Titles: ${jobTitles.length}`);
		console.log(`   Business Units: ${businessUnits.length}`);
		console.log(`   Departments: ${departments.length}`);
		console.log(`   Locations: ${locations.length}`);
		console.log(`   Legal Entities: ${legalEntities.length}`);
		console.log(`   Users: ${users.length}\n`);

		// Create lookup maps
		const jobTitleMap = new Map(jobTitles.map((jt) => [jt.name, jt.id]));
		const businessUnitMap = new Map(
			businessUnits.map((bu) => [bu.name, bu.id]),
		);
		const departmentMap = new Map(departments.map((d) => [d.name, d.id]));
		const locationMap = new Map(locations.map((l) => [l.name, l.id]));
		const legalEntityMap = new Map(legalEntities.map((le) => [le.name, le.id]));
		const userEmailMap = new Map(users.map((u) => [u.email, u.id]));
		const userNameMap = new Map(
			users.map((u) => {
				// Extract first part of name for matching with reporting to
				const nameParts = u.name.split(" ");
				return [nameParts[0], u.id];
			}),
		);

		console.log(" Processing employees...\n");

		let successCount = 0;
		let skipCount = 0;
		const failedEmployees: string[] = [];

		for (const emp of employeeData) {
			try {
				// Skip if no email and cannot identify user
				if (!emp.email || emp.email.trim() === "") {
					console.log("  Skipping employee with no email");
					skipCount++;
					continue;
				}

				// Get user ID
				const userId = userEmailMap.get(emp.email.toLowerCase());
				if (!userId) {
					console.log(
						`  User not found for email: ${emp.email} (Skipping - user must exist first)`,
					);
					skipCount++;
					continue;
				}

				// Check if employee record already exists
				const existingEmployee = await db
					.select()
					.from(employee)
					.where(eq(employee.userId, userId));

				if (existingEmployee.length > 0) {
					console.log(`  Employee record already exists for: ${emp.email}`);
					skipCount++;
					continue;
				}

				// Get reference IDs
				const jobTitleId = jobTitleMap.get(emp.jobTitle);
				const businessUnitId = businessUnitMap.get(emp.businessUnit);
				const departmentId = departmentMap.get(emp.department);
				const locationId = locationMap.get(emp.location);
				const legalEntityId = legalEntityMap.get(emp.legalEntity);

				if (!jobTitleId) {
					console.log(` Job Title not found: ${emp.jobTitle}`);
					failedEmployees.push(emp.email);
					continue;
				}
				if (!businessUnitId) {
					console.log(` Business Unit not found: ${emp.businessUnit}`);
					failedEmployees.push(emp.email);
					continue;
				}
				if (!departmentId) {
					console.log(` Department not found: ${emp.department}`);
					failedEmployees.push(emp.email);
					continue;
				}
				if (!locationId) {
					console.log(` Location not found: ${emp.location}`);
					failedEmployees.push(emp.email);
					continue;
				}
				if (!legalEntityId) {
					console.log(` Legal Entity not found: ${emp.legalEntity}`);
					failedEmployees.push(emp.email);
					continue;
				}

				// Get reporting to user ID if available
				let reportingToUserId = null;
				if (emp.reportingTo && emp.reportingTo.trim() !== "") {
					// Try to find by full name or first name
					let foundUserId = null;

					// First try exact name match
					for (const u of users) {
						if (u.name.includes(emp.reportingTo)) {
							foundUserId = u.id;
							break;
						}
					}

					// If not found, try first part of reporting to name
					if (!foundUserId) {
						const reportingFirstName = emp.reportingTo.split(".")[0];
						foundUserId = userNameMap.get(reportingFirstName);
					}

					reportingToUserId = foundUserId;
				}

				// Create employee record
				await db.insert(employee).values({
					id: uuidv4(),
					userId,
					dateOfJoining: parseDate(emp.dateOfJoining),
					jobTitleId,
					businessUnitId,
					departmentId,
					locationId,
					legalEntityId,
					reportingToUserId,
				});

				console.log(` Created employee record for: ${emp.email}`);
				successCount++;
			} catch (error) {
				const errorMsg = error instanceof Error ? error.message : String(error);
				console.error(` Failed for ${emp.email}:`, errorMsg);
				failedEmployees.push(emp.email);
			}
		}

		console.log("\n" + "=".repeat(50));
		console.log(" Seed Summary:");
		console.log(` Successfully created: ${successCount} employee records`);
		console.log(`  Skipped: ${skipCount} employees`);
		console.log(` Failed: ${failedEmployees.length} employees`);

		if (failedEmployees.length > 0) {
			console.log("\n Failed employees:");
			failedEmployees.forEach((email) => {
				console.log(`  - ${email}`);
			});
		}

		console.log("=".repeat(50));
		console.log(" Employee seeding completed!");
	} catch (error) {
		console.error(" Seed failed:", error);
		process.exit(1);
	}
}

